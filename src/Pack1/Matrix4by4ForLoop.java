package Pack1;

public class Matrix4by4ForLoop {
	//Print a 4*4 matrix from 1 to 16 numbers using forloop or while loop?
	public static void main(String[] args) {
	
		for (int i=1; i<=4; i=i+1){
			System.out.print(i+ "  ");
		}
			System.out.println();
		for (int j=5; j<=8; j=j+1){
			System.out.print(j+ "  ");
		}	
			System.out.println();
		for (int k=9; k<=12; k=k+1){
			System.out.print(k+ " ");
		}	
			System.out.println();
		for (int l=13; l<=16; l=l+1){
			System.out.print(l+" ");
			
		}
		
		
	}

}
