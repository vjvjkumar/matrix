package Pack1;

public class MatrixPrint3by3 {
	public static void main(String[] args) {
		for(int i=1;i<=3;i++){
			System.out.print(i+" ");
		}
		System.out.println();
		for(int j=4;j<=6;j++){
			System.out.print(j+" ");
		}
		System.out.println();
		for(int k=7;k<=9;k++){
			System.out.print(k+" ");
		}
	}

}
